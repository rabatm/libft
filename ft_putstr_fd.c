#include "libft.h"

void	ft_putstr_fd(char *s, int fd)
{
	int	len;

	if (!s)
		return ;
	len = ft_strlen(s);
	while (len)
	{
		ft_putchar_fd(*s, fd);
		len--;
		s++;
	}
}
